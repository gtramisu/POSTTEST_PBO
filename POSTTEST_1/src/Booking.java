public class Booking {

    String idBooking;
    String namaCustomer;
    String namaMUA;
    String tanggal;

    public Booking(String idBooking, String namaCustomer, String namaMUA, String tanggal) {
        this.idBooking = idBooking;
        this.namaCustomer = namaCustomer;
        this.namaMUA = namaMUA;
        this.tanggal = tanggal;
    }

}
public class MakeupArtist {

    String id;
    String nama;
    String spesialisasi;
    int harga;

    public MakeupArtist(String id, String nama, String spesialisasi, int harga) {
        this.id = id;
        this.nama = nama;
        this.spesialisasi = spesialisasi;
        this.harga = harga;
    }

}